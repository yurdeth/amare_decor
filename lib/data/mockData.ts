// Helper function to initialize mock data
export function initializeMockData() {
  if (typeof window === 'undefined') return;

  const hasData = localStorage.getItem('amere-clients');

  if (!hasData) {
    localStorage.setItem('amere-clients', JSON.stringify(mockClients));
    localStorage.setItem('amere-products', JSON.stringify(mockProducts));
    localStorage.setItem('amere-events', JSON.stringify(mockEvents));
    localStorage.setItem('amere-incomes', JSON.stringify(mockIncomes));
    localStorage.setItem('amere-expenses', JSON.stringify(mockExpenses));
    console.log('Datos mock inicializados correctamente');
  }
}
